// ১ থেকে ১০০ পর্যন্ত কতগুলো সংখ্যা ২ এবং ৩ দিয়ে ভাগ যায়?
// ২ দিয়ে ভাগ যায় ১০০ / ২ = ৫০ টি ৩ দিয়ে ভাগ যায় ১০০ / ৩ = ৩৩ টি 
// কিন্তু এর উত্তর ৫০ + ৩৩ = ৮৩ না । কারন ৬, ১২, ১৮ ... এগুলো ২ বার করে গণনা করা হচ্ছে 
// সুতরাং উত্তর হবে (১০০ / ২) + (১০০ / ৩) - (১০০ /  লসাগু(২, ৩)) 

#include<bits/stdc++.h>
using namespace std;

int main(){
    int a, b, n;
    cin >> a >> b >> n;
    int lcm = a * b / gcd(a, b);
    cout << (n / a + n / b - (n / lcm)) << endl;
}